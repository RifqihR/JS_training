
//1. Try and Catch. jika try error, maka catch akan dijalankan.
try {
    //kode
} catch (error){
    // Error handling
}

//2. Finally pada try and Catch. finally akan tetap dijalankan bagaimanapun hasil dari try dan catch
try {
    //kode
} catch (error){
    // Error handling
} finally {
    // another line of code
}